//Finding Sam Average Marks
public class SamAvgMarks
{
	public static void main(String[] args)
	{

		//Giving PCM marks
		int mathMarks = 94;
		int physicsMarks = 95;
		int chemistryMarks = 96;

		//Calculating Average Marks
		double averageMarks = (mathMarks + physicsMarks + chemistryMarks)/3.0;

		//Printing Sam's Average Marks
		System.out.println("Sam average mark in PCM is "+averageMarks);
	}
}

	